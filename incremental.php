        <p>Incremental model adalah model pengembangan sistem pada
          software engineering berdasarkan requirement software yang
          dipecah menjadi beberapa fungsi atau bagian sehingga model
          pengembangannya secara bertahap. dilain pihak ada mengartikan
          model incremental sebagai perbaikan dari model waterfall dan
          sebagai standar pendekatan topdown. </p><br>

        <hr>
        <br><br>
        <p>Model ini pun juga memiliki tahapan tahapan untuk perancangan perangkat lunaknya, yaitu:
          <br> <br>
          <b>1. Requirement</b> <br>
          Requirment adalah proses tahapan awal yang dilakukan pada incremental model adalah penentuan kebutuhan atau analisis kebutuhan.
          <br><br>
          <b>2. Specification</b> <br>
          Specification adalah proses spesifikasi dimana menggunakan analisis kebutuhan sebagai acuannya.
          <br><br>
          <b>3. Architecture Design</b> <br>
          Adalah tahap selanjutnya, perancangan software yang terbuka agar dapat diterapkan sistem pembangunan per-bagian pada tahapan selanjutnya.
          <br><br>
          <b>4. Code </b><br>
          Code setelah melakukan proses desain selanjutnya ada pengkodean.
          <br><br>
          <b>5. Test </b><br>
          Test merupakan tahap pengujian dalam model ini.</p><br>

        <hr>
        <br><br>

        <p>Beberapa Kelebihan Dari Mode Incremental atara lain : <br><br>
          1. Merupakan model dengan manajemen yang sederhana <br><br>
          2. Pengguna tidak perlu menunggu sampai seluruh sistem dikirim untuk mengambil keuntungan dari sistem tersebut.
          Increment yang pertama sudah memenuhi persyaratan mereka yang paling kritis, sehingga perangkat lunak dapat
          segera digunakan. <br><br>
          3. Resiko untuk kegagalan proyek secara keseluruhan lebih rendah. Walaupun masalah masih dapat ditemukan pada
          beberapa increment. Karena layanan dengan prioritas tertinggi diserahkan pertama dan increment berikutnya
          diintegrasikan dengannya, sangatlah penting bahwa layanan sistem yang paling penting mengalami pengujian yang
          ketat. Ini berarti bahwa pengguna akan memiliki kemungkinan kecil untuk memenuhi kegagalan perangkat lunak pada
          increment sistem yang paling bawah. <br><br>
          4. Nilai penggunaan dapat ditentukan pada setiap increment sehingga fungsionalitas sistem disediakan lebih awal. <br><br>
          5. Memiliki risiko lebih rendah terhadap keseluruhan pengembagan sistem, <br><br>
          6. Prioritas tertinggi pada pelayanan sistem adalah yang paling diuji. </p><br>

        <hr>
        <br><br>

        <p>Beberapa Kekurangan Dari Model Incermental antara lain: <br><br>
          1. kemungkinan tiap bagian tidak dapat diintegrasikan <br><br>
          2. Dapat menjadi build and Fix Model, karena kemampuannya untuk
          selalu mendapat perubahan selama proses rekayasa berlangsung <br><br>
          3. Harus Open Architecture <br><br>
          4. Mungkin terjadi kesulitan untuk memetakan kebutuhan pengguna ke dalam rencana
          spesifikasi masing-masing hasil increment.</p><br><br>

