        <p>Beberapa Kelebihan Dari Model SCRUM atara lain : <br><br>
          1. SCRUM dapat membantu perusahaan Anda dalam menghemat waktu
          dan biaya (dalam hal ini uang). Biaya overhead dari proses dan
          manajemen sangat minim sehingga dapat mengarahkan kita kepada
          hasil yang lebih cepat dan lebih murah.<br><br>
          2. Dengan menggunakan metode SCRUM, Anda dapat
          mentranformasikan bisnis yang sulit untuk diukur menjadi
          mudah untuk dikembangkan.<br><br>
          3. Pada metode SCRUM, pergerakan pengembangan cutting
          edge dapat dengan cepat dikodekan dan diuji menggunakan metode
          ini. Bagaikan kesalahan yang mudah untuk diperbaiki. <br><br>
          4. Dengan menggunakan SCRUM, Anda dapat mengontrol dan
          memonitoring aktivitas peningkatan dan penurunan beban
          pekerjaan yang bisa terjadi kapan saja. <br><br>
          5. Seperti metodologi agile pada umumnya,
          SCRUM merupakan metode iterative yang membutuhkan
          feedback secara berkelanjutan dari user atau pengguna.<br><br>
          6. Dengan adanya short sprint dan constant feedback,
          SCRUM dapat dengan mudah mengatasi setiap perubahan
          yang terjadi.<br><br>
          7. Dengan adanya daily scrum meeting, memungkinan SCRUM
          untuk mengukur produktvitas individu, hal ini mengarah pada
          peningkatan produktivitas dari setiap anggota tim.<br><br>
          8. Dengan SCRUM, setiap ada masalah yang timbul
          dapat di identifikasi dengan baik pada pertemuan harian dan
          oleh karena itu setiap masalah dapat di selesaikan
          dengan cepat.<br><br>
          9. Dengan menggunakan metode SCRUM, Anda dapat dengan
          mudah untuk mengirim produk berkualitas sesuai dengan
          waktunya.<br><br>
          10. SCRUM dapat bekerja dengan berbagai teknologi dan
          bahasa pemrograman. Namun secara khusus berguna untuk
          pengembangan proyek dengan teknologi web 2.0 ataupun
          media proyek baru lainnya.<br><br>
        </p><br>

        <hr>
        <br><br>

        <p>Beberapa Kekurangan Dari Model SCRUM antara lain: <br><br>
          1. SCRUM bisa menjadi salah satu penyebab utama terjadinya
          scope creep, kecuali ada tanggal akhir tertentu. Stakeholder
          proyek atau manajemen akan terus menuntut fungsi dan fitur
          baru untuk disampaikan. <br><br>
          2. Setiap tugas harus didefinisikan dengan baik, karena
          hal ini dapat mempengaruhi perkiraan biaya dan waktu
          pengerjaan proyek. Jika tidak didefinisikan dengan baik
          maka semua hal tersebut tidak akan akurat. Dalam kasus seperti ini,
          biasanya tugas dapat tersebar di beberapa sprint. <br><br>
          3. Jika anggota tim Anda tidak berkomitmen dengan baik,
          maka proyek Anda tidak akan selesai atau bahkan bisa gagal. <br><br>
          4. Metode SCRUM ini hanya membutuhkan anggota tim yang
          sudah berpengalaman, jika tim Anda berisi orang-orang yang
          masih pemula maka proyek tidak dapat selesai sesuai dengan waktunya.<br><br>
          5. SCRUM dapat bekerja dengan baik jika seorang Scrum
          Master dapat mempercayai tim yang mereka kelola.
          Jika Scrum Master terlalu mengontrol secara ketat, hal
          ini dapat menyebabkan tim menjadi tertekan dan stress,
          sehingga mengakibatkan demoralisasi dan kegagalan dari
          proyek tersebut..<br><br>
          6. Jika sering terjadi pergantian anggota tim saat
          pengembangan proyek berlangsung, hal ini dapat menyebabkan
          efek yang kurang baik bagi perkembangan proyek tersebut,
          proyek akan semakin lama selesai dari waktunya.<br>
        </p><br><br>

