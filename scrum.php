        <p>SCRUM adalah sebuah metode iteratif yang termasuk dalam metode Agile
          tentang cara mengelola dan menjalankan sebuah proyek. Dengan menggunakan metode Scrum,
          kamu bisa mengelola segala macam proyek mulai dari pembuatan software, website, hardware,
          marketing, hingga event planning. Menggunakan Scrum merupakan framework terbaik untuk
          merevolusi cara kerja tim dalam menyelesaikan pekerjaan. Dalam metodologi Scrum setiap
          “Sprint” dimulai dengan meeting singkat untuk perencanaan dan diakhiri dengan review.</p><br>

        <hr>
        <br><br>
        <p>Ada beberapa tahap yang harus dilakukan dalam melakukan Scrum Development. Berikut cara kerja Scrum, yaitu:
          <br> <br>
          <b>1. Tentukan Tim Scrum</b>
          <br><br>
          <b>2. Tentukan Waktu Sprint</b>
          <br><br>
          <b>3. Tunjuk Seorang Scrum Master</b>
          <br><br>
          <b>4. Tunjuk Pemilik Produk (Product Owner) </b>
          <br><br>
          <b>5. Buat Product Backlog Awal</b>
          <br><br>
          <b>6. Rencanakan dan Mulai Sprint</b>
          <br><br>
          <b>7. Tutup dan Mulai Sprint Berikutnya </b><br><br>
        </p><br>

