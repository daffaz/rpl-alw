        <p>Merupakan model pengembangan perangkat lunak yang
          didasarkan pada hubungan antara setiap fase pengembangan
          siklus hidup yang tercantum dalam model Watterfall yang
          merupakan pengembangan perangkat lunak dan fase yang terkait
          pengujian. Bisa dikatakan model ini merupakan perluasan dari
          model waterfall. Disebut sebagai perluasan karena tahap-tahapnya
          mirip dengan yang terdapat dalam model waterfall. Jika dalam model
          waterfall proses dijalankan secara linear, maka dalam model V
          proses dilakukan bercabang. </p><br>

        <hr>
        <br><br>
        <p>Tahapan pada V Model dibagi menjadi 2 garis besar yaitu tahap
          Verifikasi dan Validasi atau testing. Tahap Verfiikasi mengacu
          kepada usaha penyesuaian spesifikasi software dengan kebutuhan
          klien/konsumen, tahapan ini meliputi serangkaian kegiatan sebagai berikut:
          <br> <br>
          <b>1. Business Case</b> <br>
          Merupakan tahapan awal yang menggambarkan kebutuhan/harapan konsumen terhadap
          sistem yang akan dikembangkan, termasuk manfaat sistem terhadap konsumen dan
          perkiraan biaya yang harus disediakan.
          <br><br>
          <b>2. Requirement</b> <br>
          pada fase ini klien mendapatkan gambaran atau diminta memberikan gambaran
          kebutuhan yang diharapkan dapat dipenuhi oleh software, baik kebutuhan fungsional
          maupun non fungsional.
          <br><br>
          <b>3. Analisis Informasi</b> <br>
          Setelah diperoleh spesifikasi sistem dari fase requirement, selanjutnya aktivitas
          difokuskan bagaimana cara kerja software untuk memenuhi kebutuhan tersebut,
          termasuk metode, hardware dan software apa saja yang diperlukan untuk mencapai
          kebutuhan yang sudah didefinisikan.
          <br><br>
          <b>4. Perancangan Sistem </b><br>
          pada tahapan ini akan dibuat rancangan software secara lebih terinci sesuai
          spesifikasi yang sudah disepakati.
          <br><br>
          <b>5. Unit Design </b><br>
          merancang setiap elemen/unit software termasuk rancangan modul/program,
          antarmuka, database dan lain-lain.
          <br><br>
          <b>6. Unit Design </b><br>
          merealisasikan hasil rancangan menjadi satu aplikasi/program tertentu.
          Tahapan Validasi merupakan serangkaian tahapan yang mengacu kepada kesesuaian
          software dengan spesifikasi yang sudah ditetapkan. Tahapan ini dicapai melalui
          serangkaian pengujian/testing sebagai berikut:
          <br><br>
          <b>&bull; Unit Test</b><br>
          menguji setiap komponen/unit program apakah sesuai dengan rancangan unit yang
          sudah ditetapkan. Secara teoritis seharusnya pengujian dilakukan oleh orang
          tertentu yang bertugas sebagai software tester, tetapi dalam kenyataannya
          seringkali unit testing dilakukan oleh programmer sendiri.
          <br><br>
          <b>&bull; Interface Test</b><br>
          setelah semua komponen diuji secara terpisah, tahapan selanjutnya dilakukan
          interface test untuk melihat sejauh mana setiap komponen dapat berinteraksi
          satu sama lain sesuai dengan fungsi yang diharapkan.
          <br><br>
          <b>&bull; System Test</b><br>
          setelah semua interface berjalan dengan baik, selanutnya dilakukan system
          test untuk melihat sejauh mana sistem/software dapat memenuhi kebutuhan secara
          keseluruhan. System testing bersifat menyeluruh dan tidak dapat dilakukan
          berdasarkan fungsionalitas sistem yang diuji secara terpisah.
          <br><br><br>
          Aktivitas pada system testing termasuk melakukan pengujian hal-hal berikut:
          <br><br>
          <b>1. Performance </b><br>
          Apakah kinerja sistem sesuai dengan target yang sudah didefinisikan sebelumnya.
          <br><br>
          <b>2. Volume </b><br>
          Apakah software/sistem dapat menampung volume informasi yang cukup besar.
          <br><br>
          <b>3. Stress </b><br>
          Apakah software/sistem dapat menampung sejumlah informasi pada waktu-waktu tertentu.
          <br><br>
          <b>4. Documentation </b><br>
          apakah semua dokumentasi penting sudah disiapkan.
          <br><br>
          <b>5. Robustness </b><br>
          Apakah software/sistem cenderung stabil pada berbagai kondisi diluar dugaan/ekstrim.
          <br><br>

          • Acceptance test merupakan aktivitas untuk menguji sejauh mana sistem/software
          dapat membantu memecahkan business case, dalam artian apakah sistem/software
          tersebut sudah sesuai dengan harapan konsumen/klien dan sejauh mana manfaat
          sistem/software ini bagi klien. Test ini sering kali disebut sebagai User Acceptance
          Test (UAT).
          <br><br>
          • Release testing: test ini dilakukan untuk menguji sejauh mana sistem/software
          dapat mendukung aktivitas organisasi dan berjalan dengan harmonis sesuai dengan
          kegiatan rutin organisasi.
          <br><br>
        </p>


