        <p>Beberapa Kekurangan Dari VShaped Model antara lain: <br><br>
          1. V model hanya bisa digunakan sekali dalam suatu proyek hal tersebut
          disebabkan kerena V model merupakan model yang project oriented. <br><br>
          2. V model bersifat terlalu fleksibel sehingga mengakibatkan beberapa
          aktivitas-aktivitas yang digambarkan dalam V model menjadi terlalu abstrak.
          Hal tersebut mengakibatkan tidak bisa diketahui dengan jelas apa yang termasuk
          dalam activity tersebut dan apa yang tidak.</p><br><br>

