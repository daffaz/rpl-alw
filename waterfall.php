        <p>Waterfall adalah metode pengembangan perangkat lunak yang
          memungkinkan pembuatan sistem dilakukan secara terstuktur
          dan sistematis (berurutan) sesuai dengan siklus pengembangan
          yang ada. Metode ini disebut waterfall atau air terjun karena
          dalam prosesnya, sistem akan dibuat berurutan setahap demi setahap</p><br>

        <hr>
        <br><br>
        <p>Model ini pun juga memiliki tahapan tahapan untuk perancangan perangkat lunaknya, yaitu
          <br> <br>
          <b>1. Requirement</b><br>
          Pada tahap ini pengembang harus mengetahui seluruh informasi mengenai kebutuhan
          sofatware seperti kegunaan software yang diinginkan oleh pengguna dan batasan software.
          <br><br>
          <b>2. Design</b><br>
          Desain dilakukan sebelum proses coding dimulai. Ini bertujuan untuk memberikan
          gambaran lengkap tentang apa yang harus dikerjakan dan bagaimana tampilan dari
          sebuah sistem yang diinginkan.
          <br><br>
          <b>3. Implementation</b><br>
          Proses penulisan code ada di tahap ini. Pembuatan software akan dipecah menjadi
          modul-modul kecil yang nantinya akan digabungkan dalam tahap selanjutnya.
          <br><br>
          <b>4. Integration & Testing</b><br>
          Pada tahap keempat ini akan dilakukan penggabungan modul-modul yang sudah dibuat
          sebelumnya. Setelah itu akan dilakukan pengujian yang bertujuan untuk mengetahui
          apakah software sudah sesuai desain yang diinginkan dan apakah masih ada
          kesalahan atau tidak.
          <br><br>
          <b>5. Operation & Maintenance</b><br>
          Operation & Maintenance adalah tahapan terakhir dari metode pengembangan waterfall.
          Di sini software yang sudah jadi akan dijalankan atau dioperasikan oleh penggunanya.
          Disamping itu dilakukan pula pemeliharaan <br><br>
        </p><br>

        <hr>
        <br><br>

        <p>Di bawah ini adalah beberapa kelebihan mengembangkan software dengan metode waterfall, antara lain :<br><br>
          1. Metode pengembangan yang paling handal dan paling lama digunakan oleh para developer<br><br>
          2. Cocok untuk membuat software dengan skala besar<br><br>
          3. Cocok untuk mengembangkan sistem yang bersifat generic<br><br>
          4. Pengerjaan proyek sistem akan mudah dikontrol dan terjadwal dengan baik<br><br>
        </p><br>

        <hr>
        <br><br>

        <p>Beberapa Kekurangan Dari Model Extreme Programming antara lain: <br><br>
          1. Persyaratan sistem harus digambarkan dengan jelas<br><br>
          2. Rincian proses harus benar-benar jelas dan tidak boleh berubah<br><br>
          3. Sulit untuk beradaptasi jika ada perubahan spesifikasi pada suatu tahapan pengembangan. <br>
        </p><br><br>

